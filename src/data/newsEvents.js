// Helpers
export const getWeekEvents = (mondayDate) => {
  const events = [];
  for (let i = 0; i < 5; i++) {
    const d = new Date(mondayDate);
    d.setDate(d.getDate() + i);
    const key = d.toISOString().split('T')[0];
    const dayName = ['Mon','Tue','Wed','Thu','Fri'][i];
    const dayEvents = NEWS[key] || [];
    dayEvents.forEach(ev => events.push({ ...ev, date: key, day: dayName }));
  }
  return events;
};

export const getMondayOf = (dateStr) => {
  const d = new Date(dateStr + 'T12:00:00');
  const day = d.getDay();
  const diff = day === 0 ? -6 : 1 - day;
  d.setDate(d.getDate() + diff);
  return d.toISOString().split('T')[0];
};

export const getWeekLabel = (mondayStr) => {
  const mon = new Date(mondayStr + 'T12:00:00');
  const fri = new Date(mondayStr + 'T12:00:00');
  fri.setDate(fri.getDate() + 4);
  const fmt = (d) => d.toLocaleDateString('en-GB', { day:'2-digit', month:'short' });
  return `${fmt(mon)} – ${fmt(fri)}, ${mon.getFullYear()}`;
};

export const addWeeks = (mondayStr, n) => {
  const d = new Date(mondayStr + 'T12:00:00');
  d.setDate(d.getDate() + n * 7);
  return d.toISOString().split('T')[0];
};
